/**
  *****************************************************************************
  * @file   main.c
  * @brief  Application entry point for the Energy Analyzer
  * @author NU Technology Inc.
  * @date   Feb 2026
  * @details
  *
  * Initializes STM32L431 peripherals (UART1 for nRF, UART2 for Modbus),
  * creates FreeRTOS tasks (meterTask, nrfTask, ledRelayTask), and
  * starts the RTOS scheduler. System clock runs at 4 MHz MSI.
  *
  ******************************************************************************
  * 
  * SYSTEM WORKFLOW (STM32 side)
  *
  * UART2 (RS-485 / Modbus RTU)  <->  ENTES meter
  * UART1 (TTL UART)             <->  nRF9151 (Wirepas node)
  *
  * Runtime flow:
  *  - meterTask periodically calls MeterReader_ReadAll()
  *  - MeterReader uses Modbus_SendAndReceive() for each register group
  *  - USART2 IRQ accumulates bytes into buf_uart2; IDLE interrupt marks frame end
  *  - Modbus_ParseResponse() validates frame + CRC and returns payload
  *  - When nRF requests (UART1 query), nrfTask builds and sends response frame
  *
  * Timing/assumptions:
  *  - Modbus frame boundary is detected via USART2 IDLE-line interrupt
  *  - RS-485 direction is controlled by METER_EN pin (DE/RE)
  ******************************************************************************
  */

